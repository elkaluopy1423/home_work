//In Dart, you can concatenate (join) strings in two main ways:

//first way :

void main() {
  String firstName = "Omar";
  String lastName = "Elkaluopy";
  print(firstName + " " + lastName); // Output: Omar Elkaluopy

  //second way :
  String fullName = "$firstName $lastName";
  print(fullName); // Output: Omar Elkaluopy
}
